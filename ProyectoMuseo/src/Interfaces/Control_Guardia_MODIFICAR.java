    public void LimpiarCampos() {
        txtcodC.setText("");
        txtdescripcion.setText("");
        txtIdGuardia.setText("");
        txtCod_area.setText("");
        jCalendarFecha.setDate(null);
    }

    public void HabilitarCampos_deTexto() {

        txtdescripcion.setEditable(true);
        txtIdGuardia.setEditable(true);
        txtCod_area.setEditable(true);
    }

    public void buscar(ObjectContainer basep) {//cargardatos

        btnmodificar.setEnabled(false);
        String codC;
        codC = txtcodC.getText();

        Control_Guardia_CRUD areacrud = new Control_Guardia_CRUD();

        if (txtcodC.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Ingrese un ID/Codigo");
        } else {

            if (areacrud.comprobarArea(basep, codC) == 0) {

                JOptionPane.showMessageDialog(null, "El Comerciante no existe en la base de datos");
                LimpiarCampos();

            } else {

                Control_guardia Ebuscar = new Control_guardia(codC, null, null, null, null);

                ObjectSet result = basep.get(Ebuscar);
                for (int i = 0; i < result.size(); i++) {

                    Control_guardia miE = new Control_guardia();

                    miE = (Control_guardia) result.get(i);
                    txtcodC.setText(miE.getCod_control_guardia());
                    jCalendarFecha.setDate(miE.getFecha());
                    txtdescripcion.setText(miE.getObservaciones());
                    txtIdGuardia.setText(miE.getId_guardia());
                    txtCod_area.setText(miE.getCod_exposicion());
                    
                    btnmodificar.setEnabled(true);
                    //Hacer editable los campos de texto
                    
                    HabilitarCampos_deTexto();
                    txtcodC.setEditable(false);
                }

            }

        }
    }

    public void Modificar_Control(ObjectContainer basep) {

        Control_guardia Emodi = new Control_guardia(txtcodC.getText(), null, null, null, null);
        ObjectSet result = basep.get(Emodi);

        Control_guardia Emodificar = (Control_guardia) result.next();
        Emodificar.setFecha(jCalendarFecha.getDate());
        Emodificar.setObservaciones(txtdescripcion.getText());
        Emodificar.setId_guardia(txtIdGuardia.getText());
        Emodificar.setCod_exposicion(txtCod_area.getText());

        basep.set(Emodificar);
        JOptionPane.showMessageDialog(null, "El Control fue modificado exitosamente");
        
        LimpiarCampos();
    }

    

