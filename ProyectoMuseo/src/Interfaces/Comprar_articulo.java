    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

    int[] filasSeleccionadas = tablaArticulos.getSelectedRows();

    if (filasSeleccionadas.length > 0) {
        for (int fila : filasSeleccionadas) {
            DefaultTableModel modelo = (DefaultTableModel) tablaArticulos.getModel();
            String codigo = modelo.getValueAt(fila, 0).toString();
            String nombre = modelo.getValueAt(fila, 1).toString();
            double precio = Double.parseDouble(modelo.getValueAt(fila, 6).toString());

        
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            String fechaActual = sdf.format(new Date());

          
            FACTURA_ARTUCULOS facturaInterfaz = new FACTURA_ARTUCULOS();
            facturaInterfaz.setFecha(fechaActual);
            facturaInterfaz.agregarCodigo(codigo);
            facturaInterfaz.agregarNombreArticulo(nombre);
            facturaInterfaz.agregarPrecio(precio);

           
            ObjectContainer BaseD = Db4o.openFile(PaginaPrincipal.direccionBD);
            Articulo_info miArticulo = new Articulo_info();
            miArticulo.setCod_articulo(codigo);
            miArticulo.setNombre(nombre);
            miArticulo.setDescripcion(""); 
            miArticulo.setExistencia(0); 
            miArticulo.setExis_max(0); 
            miArticulo.setExis_min(0); // Agrega la existencia mínima que corresponda
            miArticulo.setPrecio(precio); // Actualizamos el precio en la base de datos
            BaseD.store(miArticulo);
            BaseD.commit();
            BaseD.close();
            facturaInterfaz.setVisible(true);
            
        }

        JOptionPane.showMessageDialog(this, "Artículos agregados a la factura.");
    } else {
        JOptionPane.showMessageDialog(this, "Por favor, seleccione al menos un artículo de la tabla.");
    }
    

    }//GEN-LAST:event_jButton1ActionPerformed

