    public void filtro(ObjectContainer BaseD) {
        if (filtrocmb.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Selección invalida");
        } else {
            if (filtrocmb.getSelectedIndex() == 1) {
                 Escultura EscBuscar = new Escultura(null, 0, 0,null,  null, null, null);
                ObjectSet result = BaseD.get(EscBuscar);
                mostrarDatos(result);

            } else {
                if (filtrocmb.getSelectedIndex() == 2) {
                    String codigo = JOptionPane.showInputDialog("Ingrese el Codigo a consultar");
                    Escultura Esbuscar = new Escultura(codigo, 0, 0, null, null, null, null);
                    ObjectSet result = BaseD.get(Esbuscar);
                    mostrarDatos(result);

                } else {
                    if (filtrocmb.getSelectedIndex() == 3) {
                        String nombreEs = JOptionPane.showInputDialog("Ingrese el nombre a consultar");
                        Escultura Esbuscar = new Escultura(null, 0, 0, nombreEs, null, null, null);
                        ObjectSet result = BaseD.get(Esbuscar);
                        mostrarDatos(result);
                    }

                }

            }

        }
    }

    public void mostrarDatos(ObjectSet result) {
        DefaultTableModel model = (DefaultTableModel) tblescultura.getModel();
        model.setRowCount(0); // Limpiar la tabla
        String matrizEscultura[][] = new String[result.size()][6];
        if (result.size() == 0) {
            JOptionPane.showMessageDialog(null, "No esculturas existentes");
        }

        for (int i = 0; i < result.size(); i++) {
            Escultura miescultura = new Escultura();
            miescultura = (Escultura) result.get(i);
            matrizEscultura[i][0] = miescultura.getCodigo();
            matrizEscultura[i][1] = miescultura.getNombre();
            matrizEscultura[i][2] = miescultura.getDescripcion();
            matrizEscultura[i][3] = miescultura.getMaterial();
            matrizEscultura[i][4] = String.valueOf(miescultura.getPeso());
            matrizEscultura[i][5] = String.valueOf(miescultura.getAltura());
            matrizEscultura[i][6] = String.valueOf(miescultura.getFecha_creacion());

            tblescultura.setModel(new javax.swing.table.DefaultTableModel(matrizEscultura, new String[]{"codigo", "nombre", "descripcion", "material", "peso", "fecha_creacion"}));
        }

    }

    public void eliminarEscultura(ObjectContainer BaseD) {

        if (codigotxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Ingrese un Codigo");
        } else {
            String codigo = codigotxt.getText();
            Escultura EsEliminar = new Escultura(codigo, 0, 0, null, null, null, null);
            ObjectSet result = BaseD.get(EsEliminar);

            if (comprobarEscultura(BaseD, codigo) == 0) {
                JOptionPane.showMessageDialog(null, "La escultura no existe en la base de datos");

            } else {
                Escultura EliminarEs = (Escultura) result.next();
                BaseD.delete(EliminarEs);
                JOptionPane.showMessageDialog(null, "La escultura fue eliminada exitosamente");
            }

        }

        //Borrar el campo de texto
        codigotxt.setText("");

    }

    public static int comprobarEscultura(ObjectContainer BaseD, String codigo) {
        Escultura Esbuscar = new Escultura(codigo, 0, 0, null, null, null, null);
        ObjectSet result = BaseD.get(Esbuscar);
        return result.size();

    }

